# Coze Studio 部署指南
# 在 PowerShell 中逐条执行

# ============================================
# 1️⃣ 先切到工作区目录
# ============================================
cd F:\openclaw-data\.openclaw\workspace

# ============================================
# 2️⃣ 下载 Coze Studio 源码
# ============================================
git clone https://github.com/coze-dev/coze-studio.git

# ============================================
# 3️⃣ 进入项目目录，复制环境变量配置
# ============================================
cd coze-studio
cp .\docker\.env.example .\docker\.env

# ============================================
# 4️⃣ 启动（第一次会拉取镜像，需要几分钟）
# ============================================
docker compose -f .\docker\docker-compose.yml up -d

# ============================================
# 5️⃣ 检查服务是否正常启动
# ============================================
docker compose -f .\docker\docker-compose.yml ps

# ============================================
# 访问地址：
#   http://localhost:8888/sign  → 注册账号
#   http://localhost:8888/      → 进入Coze Studio
# ============================================
